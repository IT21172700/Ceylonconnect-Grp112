import mongoose from "mongoose";

const placeSchema = new mongoose.Schema({
  placeName: {
    type: String,
    required: true,
  },
  province: {
    type: String,
    required: true,
  },
  district: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
});

const Place = mongoose.model("Place", placeSchema);

export default Place;
